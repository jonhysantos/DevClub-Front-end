/*
    Operadores de comparação

    == -> compara o valor, mas não o tipo
    === -> Totalmente igual, compara o valor e o tipo
    != -> Diferente // Ele compara o valor, mas não o tipo
    !== -> Totalmente diferente // compara o valor e o tipo
    >  -> maior
    <  -> menor
    >=  -> maior ou igual
    <=  -> menor ou igual

*/